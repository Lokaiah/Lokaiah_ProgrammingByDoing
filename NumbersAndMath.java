package basicsAndPrinting;


public class NumbersAndMath 
{

	
	public static void main(String[] args)
	{
		
		System.out.println(5%2);

		System.out.println(2%5);

		System.out.println(-5%2);

		System.out.println(5%-2);

		System.out.println(-5%-2);


		System.out.println(5.54321%2);

		System.out.println(5%2.4);
	
		System.out.println(0%2.4);

		System.out.println(5%0.00000000000000000000000000000000000000000000000000000000000001);

	}


}
